package IFELSE;

public class IfelseClass {

	public static void main(String[] args) {
		/*
		 ==  is equal to
		 !=  is not equal 
		 >   is greater than
		 <   is less than
		 >=  is greater than or equal to
		 >=   is less than or equal to
		 */
		int x=10;
		if(x == 20) {
			System.out.println("Condition is True");
		}
		else {
			System.out.println("Condition is False");
		}

	}

}
