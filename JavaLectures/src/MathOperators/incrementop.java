package MathOperators;

public class incrementop {

	public static void main(String[] args) {
		int x=10;
		System.out.println(x++); //10
		System.out.println(x); //11
		System.out.println(++x); //12
		x = x+5; //17
		System.out.println(x);
		x += 5; //22
		System.out.println(x);
	}

}
